  // ── Current locale state ────────────────────────────────────────────────────
  let _locale = 'en';

  // ── Core translate function ─────────────────────────────────────────────────
  /**
   * Translate a key with optional variable substitution.
   * Fallback: current locale → 'en' → raw key.
   *
   * @param {string} key - dot-notation key e.g. 'settings.show_time'
   * @param {Object} [vars] - variables to substitute e.g. {name: 'London'}
   * @returns {string}
   */
  function t(key, vars) {
    const locTable = TRANSLATIONS[_locale] || {};
    const enTable  = TRANSLATIONS.en || {};
    let str = locTable[key] ?? enTable[key] ?? key;
    if (vars) {
      str = str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? vars[k] : `{${k}}`));
    }
    return str;
  }

  /**
   * Translate a WMO weather code to a localized short label.
   * @param {number|string} code
   * @returns {string}
   */
  function tWmo(code) {
    return t(`wmo.${code}`);
  }

  // ── Locale getters / setters ────────────────────────────────────────────────
  function getLocale() { return _locale; }

  /**
   * Set locale, persist to localStorage, update html[lang] and html[dir].
   * @param {string} lang
   */
  function setLocale(lang) {
    if (!SUPPORTED_LANGS.includes(lang)) lang = 'en';
    _locale = lang;
    try { localStorage.setItem('sky_lang', lang); } catch(_) {}
    const html = document.documentElement;
    html.setAttribute('lang', lang);
    html.setAttribute('dir', RTL_LANGS.has(lang) ? 'rtl' : 'ltr');
  }

  /**
   * Detect language from localStorage → navigator.languages → 'en'.
   * @returns {string}
   */
  function detectLanguage() {
    // 1. Saved preference
    try {
      const saved = localStorage.getItem('sky_lang');
      if (saved && SUPPORTED_LANGS.includes(saved)) return saved;
    } catch(_) {}

    // 2. Browser languages
    const navLangs = navigator.languages || [navigator.language || 'en'];
    for (const lang of navLangs) {
      const base = lang.split('-')[0].toLowerCase();
      if (SUPPORTED_LANGS.includes(base)) return base;
    }

    return 'en';
  }

  /**
   * Scan the DOM for data-i18n* attributes and apply translations.
   *
   * Supported attributes:
   *   data-i18n="key"              → element.textContent
   *   data-i18n-html="key"        → element.innerHTML (use sparingly)
   *   data-i18n-placeholder="key" → input placeholder
   *   data-i18n-attr="attr:key[,attr:key]" → arbitrary attributes
   *   data-i18n-aria="key"        → aria-label
   */
  function applyTranslations() {
    // textContent
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const str = t(key);
      if (str !== key) el.textContent = str;
    });

    // innerHTML (for &amp; etc.)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      const str = t(key);
      if (str !== key) el.innerHTML = str;
    });

    // placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const str = t(key);
      if (str !== key) el.placeholder = str;
    });

    // aria-label
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      const str = t(key);
      if (str !== key) el.setAttribute('aria-label', str);
    });

    // arbitrary attributes: data-i18n-attr="title:key,aria-label:key2"
    document.querySelectorAll('[data-i18n-attr]').forEach(el => {
      const pairs = el.getAttribute('data-i18n-attr').split(',');
      pairs.forEach(pair => {
        const colonIdx = pair.indexOf(':');
        if (colonIdx < 0) return;
        const attr = pair.slice(0, colonIdx).trim();
        const key  = pair.slice(colonIdx + 1).trim();
        const str  = t(key);
        if (str !== key) el.setAttribute(attr, str);
      });
    });
  }

  /**
   * Get a localized short day abbreviation (e.g. 'Sun', '日') for day index 0-6 (0=Sun).
   * Uses Intl.DateTimeFormat with the current locale.
   *
   * @param {number} dayIndex - 0 (Sunday) through 6 (Saturday)
   * @returns {string}
   */
  function getDayAbbr(dayIndex) {
    // Create a date that falls on the requested weekday.
    // 2023-01-01 was a Sunday (dayIndex 0 = Sun, 1 = Mon, …)
    const date = new Date(Date.UTC(2023, 0, 1 + dayIndex));
    try {
      return new Intl.DateTimeFormat(_locale, { weekday: 'short', timeZone: 'UTC' }).format(date);
    } catch (_) {
      return ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'][dayIndex] || '';
    }
  }

